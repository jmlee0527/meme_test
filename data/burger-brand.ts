const targets = (dominant: Partial<BurgerScores>): BurgerScores => {
  const keys: BurgerTrait[] = ["trendy", "premium", "value", "stability", "activity", "emotion", "friendliness", "challenge", "creativity", "practicality", "uniqueness", "leadership"];
  return keys.reduce((acc, key) => ({ ...acc, [key]: dominant[key] ?? BASE }), {} as BurgerScores);
};

