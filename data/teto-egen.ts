export const TETO_EGEN_QUIZ_SIZE = 14;
export const tetoEgenDifficultyQuota = { easy: 4, medium: 6, hard: 4 } as const;

export const getTetoEgenQuestion = (id: string) => tetoEgenQuestions.find((question) => question.id === id);
export const getTetoEgenResultProfile = (slug: string) => tetoEgenResultProfiles.find((profile) => profile.slug === slug);
export const getTetoEgenResultByPercent = (tetoPercent: number) =>
  tetoEgenResultProfiles.find((profile) => tetoPercent >= profile.minTeto && tetoPercent <= profile.maxTeto) ??
  tetoEgenResultProfiles[tetoEgenResultProfiles.length - 1];

export function validateTetoEgenQuestionBank() {
  const ids = new Set(tetoEgenQuestions.map((question) => question.id));
  if (ids.size !== tetoEgenQuestions.length) throw new Error("테토·에겐 문제은행에 중복 id가 있습니다.");
  const counts = tetoEgenQuestions.reduce(
    (acc, question) => ({ ...acc, [question.difficulty]: acc[question.difficulty] + 1 }),
    { easy: 0, medium: 0, hard: 0 },
  );
  if (counts.easy < tetoEgenDifficultyQuota.easy || counts.medium < tetoEgenDifficultyQuota.medium || counts.hard < tetoEgenDifficultyQuota.hard) {
    throw new Error(`테토·에겐 문제은행 난이도 구성이 부족합니다: ${JSON.stringify(counts)}`);
  }
  for (let percent = 0; percent <= 100; percent += 1) {
    if (!tetoEgenResultProfiles.some((profile) => percent >= profile.minTeto && percent <= profile.maxTeto)) {
      throw new Error(`테토력 ${percent}%를 담당하는 결과 유형이 없습니다.`);
    }
  }
}

