export function validateBtsQuestionBank() {
  const ids = new Set(btsFanQuestions.map((question) => question.id));
  if (ids.size !== btsFanQuestions.length) throw new Error("BTS 문제은행에 중복 id가 있습니다.");
  for (const question of btsFanQuestions) {
    if (question.choices.length !== 4) throw new Error(`${question.id}: 보기는 4개여야 합니다.`);
    if (question.correctAnswer < 0 || question.correctAnswer >= 4) throw new Error(`${question.id}: 정답 인덱스가 잘못됐습니다.`);
    if (new Set(question.choices).size !== 4) throw new Error(`${question.id}: 중복 보기가 있습니다.`);
  }
  const counts = btsFanQuestions.reduce(
    (acc, question) => ({ ...acc, [question.difficulty]: acc[question.difficulty] + 1 }),
    { easy: 0, medium: 0, hard: 0 },
  );
  if (counts.easy < btsDifficultyQuota.easy || counts.medium < btsDifficultyQuota.medium || counts.hard < btsDifficultyQuota.hard) {
    throw new Error(`BTS 문제은행 난이도 구성이 부족합니다: ${JSON.stringify(counts)}`);
  }
  for (let score = 0; score <= BTS_FAN_QUIZ_SIZE; score += 1) {
    if (!btsFanGradeProfiles.some((profile) => score >= profile.minScore && score <= profile.maxScore)) {
      throw new Error(`${score}점을 담당하는 결과 등급이 없습니다.`);
    }
  }
}

